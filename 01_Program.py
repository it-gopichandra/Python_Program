# to perform arithmatic operations & display the result using simple method
x = int(input("Enter number value of x: "))
y = int(input("Enter number value of y: "))
print("Sum of x & y is: ",x+y)
print("Sub of x & y is: ",x-y)
print("Multiply of x & y is: ",x*y)
print("Devide of x & y is: ",x//y)
print("Devide of x & y is: ",x/y)
print("Module of x & y is: ",x%y)