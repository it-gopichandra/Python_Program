# to perform arithmatic operations using format method
x = int(input("Enter number value of x: "))
y = int(input("Enter number value of y: "))
sum = x + y
sub = x - y
mul = x * y
div = x / y
print('Sum of {0} and {1} is {2}'.format(x, y, sum))   
print('Sub of {0} and {1} is {2}'.format(x, y, sub))
print('Multiply of {0} and {1} is {2}'.format(x, y, mul)) 
print('Division of {0} and {1} is {2}'.format(x, y, div))